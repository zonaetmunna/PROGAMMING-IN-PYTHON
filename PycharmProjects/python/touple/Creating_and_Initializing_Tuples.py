# Creating an empty tuple
empty_tuple = ()
print(empty_tuple)

# Creating a tuple with values
my_tuple = (1, 2, 3, 4, 5)
print(my_tuple)

# You can also create a tuple without parentheses (although it's less common)
another_tuple = 10, 20, 30
print(another_tuple)

# A tuple with a single element (you need a trailing comma)
single_element_tuple = (42,)
print(single_element_tuple)

# Tuple of mixed data types
mixed_tuple = ("apple", 3.14, True)
print(mixed_tuple)



