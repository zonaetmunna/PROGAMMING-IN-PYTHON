# Addition
result_addition = 5 + 3
print(result_addition)  # Output: 8

# Subtraction
result_subtraction = 10 - 4
print(result_subtraction)  # Output: 6

# Multiplication
result_multiplication = 7 * 2
print(result_multiplication)  # Output: 14

# Division
result_division = 20 / 5
print(result_division)  # Output: 4.0 (division always results in a float)

# Modulus
result_modulus = 15 % 4
print(result_modulus)  # Output: 3 (remainder when 15 is divided by 4)
