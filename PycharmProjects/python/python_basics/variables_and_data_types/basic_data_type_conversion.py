# Integer to Float:
integer_value = 42
float_value = float(integer_value)
print(float_value)

# Float to Integer (Rounds Down):
integer_value = int(float_value)
print(integer_value)

# Integer/Float to String:
num = 42
string_num = str(num)
print(string_num)

# String to Integer/Float:
numeric_string = "123"
integer_value_string = int(numeric_string)
print(integer_value_string)
float_value = float(numeric_string)
print(float_value)

# Boolean to String:
is_true = True
string_value = str(is_true)
print(string_value)

# String to Boolean:
true_string = "True"
is_true = bool(true_string)  # This will evaluate to True
print(is_true)
