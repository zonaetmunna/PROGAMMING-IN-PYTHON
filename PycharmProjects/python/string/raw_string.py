# Regular string with escape sequence
regular_string = "This is a newline: \n and a tab: \t"
print(regular_string)

# Raw string - backslashes are treated as literal characters
raw_string = r"This is a newline: \n and a tab: \t"
print(raw_string)

# Raw string with file path
file_path = r"C:\User\Documents\file.txt"
print(file_path)
